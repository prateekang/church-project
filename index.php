        <p>I’m writing this just to get the record straight about the recent events that had <br>unfolded in our town.
            In short, yes it’s true that I gave away most of my belongings<br> to the poor and repaid those from whom I
            had swindled. So yes, I’m <br>acknowledging that I have abused my power of the position that I hold. And no,
            I<br> was not blackmailed into giving back the money. I did what I did of my own <br>accord. Since I
            understand your skepticism, I wanted to clarify my side of the story.</p>
        <p>Couple of months ago, I met Matthew, the tax collector, in Jerusalem. We have <br>
            known each other for quite a long time. Went to the same school. We actually <br>
            spent most of our childhood together. Those who know me would certify that I’m <br>
            an introvert who does not have a lot of friends. But Matthew was always an <br>exception.

            He was closest to the brother I never had. He understood me better <br>than most people.
            As tax collectors, we are always misunderstood and looked<br> down on as traitors because
            we extort money from our own people to establish <br>Roman rule. We obviously shared the
            same pain of being ostracized from our own. We would spend hours together criticizing our own<br>
            people, talking about politics and debating on the scriptures.</p>
        <p>The ‘Matthew’ that I met last in Jerusalem was indeed a changed man. <br>
            I could see that he was far more composed than I have ever seen him to be. <br>
            \In our whole course of conversation, he never brought up his share of hurt and pain.<br>
            It seemed as if he was beyond that. As if he had been healed from all the hurt and it <br>
            didn’t bother him anymore. There was so much compassion in his eyes. He mentioned that <br>
            he had quit his job. His life had been turned around since the time he decided <br>
            to follow Jesus, the Rabbi. I could not believe what I was seeing.</p>
        <p>How was this even possible? Was Jesus really the man whom Matthew<br>
            proclaimed? Seeing Matthew, I immediately recognized the depravity<br>
            of my soul, how poor and sick I was. Compared to me, Matthew was always <br>
            the better person. He never extorted money or took advantage of people like <br>
            I did. On my way back, I kept wondering if there was any redemption for a person like me.
        </p>
        <p>It was around noon yesterday, when I heard the news that Jesus was walking through my town.<br>
            People in the town had varied opinions of him. Some thought he was a prophet. Others thought<br>
            he was going to be the king of Israel who would eventually redeem us from the Romans. <br>
            Some even thought he was a false prophet claiming to be the Messiah.<br>
            I wanted to see who this Jesus was for myself. When I went to the town <br>
            square to see him, I saw a multitude of crowd around him. Well, it <br>
            was impossible to even have a glimpse of him since I am short.<br>
            I was so desperate to see him that I decided to run ahead. <br>
            I forgot everything and climbed a sycamore tree just to have <br>
            better look at him. It was also in the hope of remaining unnoticed.</p>
